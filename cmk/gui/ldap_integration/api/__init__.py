from cmk.gui.openapi.framework.registry import VersionedEndpointRegistry
from cmk.gui.openapi.restful_objects.endpoint_family import EndpointFamilyRegistry
from cmk.gui.openapi.shared_endpoint_families.ldap_connection import LDAP_CONNECTION_FAMILY

from .test_connection import ENDPOINT_TEST_LDAP_CONNECTION


def register(
    versioned_endpoint_registry: VersionedEndpointRegistry,
    endpoint_family_registry: EndpointFamilyRegistry,
) -> None:
    endpoint_family_registry.register(LDAP_CONNECTION_FAMILY)
    versioned_endpoint_registry.register(ENDPOINT_TEST_LDAP_CONNECTION)
