"""Polling-based file change watcher with debounce and auto-deploy loop.

Monitors the git working tree for changes by periodically running
``git diff --name-only`` and content hashing each dirty file.  When a
change is detected, the loop waits a short debounce interval (to let
rapid editor saves settle), then invokes the deploy pipeline.

**Design constraints:**

- Zero external dependencies: uses ``git diff`` polling via subprocess
  and ``hashlib.md5`` for content hashing.  No ``watchdog`` or
  ``inotify`` bindings required.
- Single-threaded blocking loop: ``deploy_fn`` blocks until the deploy
  cycle completes, so concurrent deploys are structurally impossible.
- Re-hashes AFTER deploy completes: catches files modified by the build
  itself (e.g. generated bytecode).
"""

from __future__ import annotations

import hashlib
import subprocess
import time
from collections.abc import Callable
from pathlib import Path
from typing import TYPE_CHECKING

from cmk.dev_deploy.core import output
from cmk.dev_deploy.state.deploy_state import load_state

if TYPE_CHECKING:
    from cmk.dev_deploy.frontend.frontend_supervisor import FrontendSupervisor
    from cmk.dev_deploy.types import DeployCycleResult, SiteInfo

_POLL_INTERVAL_S: float = 1.0
"""Seconds between ``git diff`` polls.  1s provides responsive feedback
with negligible CPU cost (~50ms per poll)."""

_DEBOUNCE_S: float = 0.3
"""Seconds to wait after detecting a change before deploying.  Lets rapid
editor saves (save-to-temp + rename, multi-file refactors) settle into a
single deploy cycle."""

_BACKOFF_INITIAL_S: float = 5.0
"""Initial backoff delay after a failed deploy cycle in watch mode."""

_BACKOFF_MAX_S: float = 60.0
"""Maximum backoff delay between retries after consecutive failures."""


def _get_untracked_files(repo_root: Path) -> str:
    """Return stdout from ``git ls-files --others --exclude-standard``.

    Captures untracked files (new files not yet ``git add``-ed) so they
    are included in the content hash scope.  Returns an empty string on
    subprocess error.
    """
    try:
        result = subprocess.run(
            ["git", "ls-files", "--others", "--exclude-standard"],
            capture_output=True,
            text=True,
            check=False,
            cwd=str(repo_root),
            timeout=5,
        )
        return result.stdout
    except (subprocess.TimeoutExpired, OSError):
        return ""


def _get_content_hash(build_commit: str | None, repo_root: Path) -> str:
    """Hash the working tree state using content-aware change detection.

    Runs ``git diff --name-only`` against *build_commit* (or ``HEAD`` when
    ``None``), includes staged changes, and also captures untracked files
    via ``git ls-files --others --exclude-standard``.  For each file in
    the combined set, hashes the filename AND the file's content with MD5
    to detect re-edits of already-dirty files.

    Deleted files (reported by ``git diff`` but absent on disk) contribute
    a ``DELETED`` sentinel instead of content, so deletion is detected as
    a change.  Files that raise ``OSError`` during read (e.g. editor
    atomic saves in progress) contribute an ``ERROR`` sentinel.

    Args:
        build_commit: 40-char git SHA to diff against, or ``None`` to
            use ``HEAD``.
        repo_root: Absolute path to the repository root.

    Returns:
        Hex digest of the combined content hash, or ``""`` on subprocess
        error (which will trigger a deploy on the next successful poll).
    """
    base = build_commit or "HEAD"
    try:
        # Unstaged + committed changes vs build_commit
        unstaged = subprocess.run(
            ["git", "diff", "--name-only", base],
            capture_output=True,
            text=True,
            check=False,
            cwd=str(repo_root),
            timeout=5,
        )
        # Staged changes
        staged = subprocess.run(
            ["git", "diff", "--name-only", "--staged"],
            capture_output=True,
            text=True,
            check=False,
            cwd=str(repo_root),
            timeout=5,
        )
    except (subprocess.TimeoutExpired, OSError):
        return ""

    # Untracked files (new files not yet git-added)
    untracked_output = _get_untracked_files(repo_root)

    # Deduplicate and sort for deterministic hashing (Pitfall 1)
    all_files = sorted(
        {
            line
            for line in (unstaged.stdout + staged.stdout + untracked_output)
            .strip()
            .splitlines()
            if line
        }
    )

    # Build hash: filename list + per-file content hashes
    hasher = hashlib.md5(usedforsecurity=False)  # nosec B324
    for relpath in all_files:
        hasher.update(relpath.encode())
        hasher.update(b"\n")
        abs_path = repo_root / relpath
        try:
            if abs_path.is_file():
                content_hash = hashlib.md5(  # nosec B324
                    abs_path.read_bytes(),
                    usedforsecurity=False,
                ).hexdigest()
                hasher.update(content_hash.encode())
            else:
                # File deleted or not accessible -- sentinel (Pitfall 2)
                hasher.update(b"DELETED")
        except OSError:
            # Race condition: editor atomic save in progress
            hasher.update(b"ERROR")
        hasher.update(b"\n")

    return hasher.hexdigest()


def _get_state_diff_base(site: SiteInfo) -> str | None:
    """Get the diff base from saved state, falling back to site.build_commit.

    Reads the deploy state file on the OMD site to determine the most
    recent successfully deployed commit.  If no state is available (first
    deploy or state cleared), falls back to the site build commit.

    Args:
        site: Resolved site information with root path and build commit.

    Returns:
        A git commit hash to diff against, or ``None`` when neither state
        nor build commit is available.
    """
    state = load_state(site.root)
    if state is not None and state.deployers:
        first_deployer = next(iter(state.deployers.values()))
        return first_deployer.git_commit
    return site.build_commit


def watch_loop(
    site: SiteInfo,
    repo_root: Path,
    deploy_fn: Callable[[], DeployCycleResult],
    supervisor: FrontendSupervisor | None = None,
) -> int:
    """Poll for file changes and auto-deploy in a blocking loop.

    Takes an initial snapshot of the working tree diff, then enters an
    infinite loop that sleeps, polls, and deploys when changes are
    detected.  The loop is single-threaded: ``deploy_fn`` blocks until
    the cycle completes, preventing concurrent deploys.

    When *supervisor* is provided (combined ``--frontend --watch`` mode),
    the loop checks supervisor health before each poll cycle and after
    each deploy cycle.  If the supervisor has crashed, the loop prints
    the crash report and returns 1.  If a deploy cycle fails, the loop
    stops the supervisor and returns 1.

    After each cycle, prints a one-line summary showing which deployers
    ran and which were skipped.  When all deployers skip, a quiet "all
    skipped" note confirms the watcher is alive.  Verbose mode (-v/-vv)
    shows per-deployer details with skip reasons via self-gating output
    functions in ``output.py``.

    Args:
        site: Resolved site info (cached for the session).
        repo_root: Absolute path to the repository root.
        deploy_fn: Callable wrapping the deploy pipeline.  Returns a
            :class:`DeployCycleResult` with exit code and cycle details.
        supervisor: Optional :class:`FrontendSupervisor` for combined
            ``--frontend --watch`` mode.  When ``None``, all supervisor
            checks are skipped (backward-compatible with standalone
            ``--watch``).

    Returns:
        Exit code: ``0`` on clean shutdown (Ctrl-C), ``1`` on supervisor
        crash or deploy failure in combined mode.
    """
    # Use state-derived diff base for polling (updates after each deploy)
    diff_base = _get_state_diff_base(site)
    last_hash = _get_content_hash(diff_base, repo_root)
    cycle = 0
    idle_polls: int = 0
    consecutive_failures: int = 0

    output.print_watch_waiting(site.name)

    try:
        while True:
            # Supervisor health check (combined --frontend --watch mode)
            if supervisor is not None and not supervisor.is_running():
                crash_lines = supervisor.get_crash_report()
                output.error("[frontend] Process exited unexpectedly")
                if crash_lines:
                    for line in crash_lines[-10:]:
                        output.error(f"  {line}")
                return 1

            time.sleep(_POLL_INTERVAL_S)
            current_hash = _get_content_hash(diff_base, repo_root)

            if current_hash == last_hash:
                idle_polls += 1
                if idle_polls > 0 and idle_polls % 60 == 0:
                    output.print_watch_heartbeat(idle_polls)
                continue

            # Change detected -- reset idle counter
            idle_polls = 0

            # Debounce: wait for rapid saves to settle, then re-hash
            time.sleep(_DEBOUNCE_S)
            current_hash = _get_content_hash(diff_base, repo_root)

            cycle += 1
            start = time.monotonic()
            output.print_watch_cycle_header(cycle)

            result = deploy_fn()

            elapsed = time.monotonic() - start

            # In combined mode, hard deploy failure (exit 1) stops everything.
            # Exit code 2 (deployed but services failed) is non-fatal — code
            # IS deployed, services just need a manual kick.
            if result.exit_code == 1 and supervisor is not None:
                output.error(
                    "[deploy] Deploy cycle failed. Stopping frontend supervisor."
                )
                supervisor.stop()
                return 1

            # Post-deploy supervisor health check
            if supervisor is not None and not supervisor.is_running():
                crash_lines = supervisor.get_crash_report()
                output.error("[frontend] Process exited during deploy cycle")
                if crash_lines:
                    for line in crash_lines[-10:]:
                        output.error(f"  {line}")
                return 1

            # Re-read state after deploy to get updated diff base
            diff_base = _get_state_diff_base(site)

            # Re-hash AFTER deploy with updated base -- catches files changed during build
            last_hash = _get_content_hash(diff_base, repo_root)

            # Rich cycle summary based on deploy result
            if result.all_skipped:
                output.print_watch_all_skipped(cycle, elapsed)
            elif result.exit_code != 0:
                output.print_watch_cycle_complete(cycle, result.exit_code, elapsed)
            else:
                output.print_watch_cycle_summary(cycle, result, elapsed)

            # Verbose per-deployer details (self-gating on VERBOSE)
            output.print_watch_cycle_verbose(result)

            if result.services_restarted > 0:
                output.print_watch_services_restarted(result.services_restarted)

            # Backoff on hard deploy failures (exit 1).  Exit code 2
            # (deployed but services failed) and 0 reset the counter.
            if result.exit_code == 1:
                consecutive_failures += 1
                backoff = min(
                    _BACKOFF_INITIAL_S * (2 ** (consecutive_failures - 1)),
                    _BACKOFF_MAX_S,
                )
                output.warn(
                    f"Deploy failed. Retrying in {backoff:.0f}s "
                    f"(attempt {consecutive_failures}, Ctrl-C to stop)"
                )
                time.sleep(backoff)
            else:
                consecutive_failures = 0

            output.print_watch_resume()

    except KeyboardInterrupt:
        if supervisor is not None:
            output.info("Stopping frontend supervisor...")
            supervisor.stop()
        output.info("Watch mode stopped.")
        return 0
