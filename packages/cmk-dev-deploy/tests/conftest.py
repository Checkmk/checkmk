"""Shared test fixtures and environment setup for cmk-dev-deploy tests.

Handles two Bazel sandbox issues:

1. Stubs out ``cmk.dev_deploy.privilege`` before any test module can trigger
   its import.  The privilege module depends on system packages (SSH, sudo)
   that are not available inside the Bazel test sandbox.

2. Seeds the manifest reader cache with a computed manifest.
   ``deploy_manifest.json`` is gitignored (generated by Bazel queries at
   build time) and must never be read by tests.  The seed manifest is built
   from checked-in data (deploy_specs.toml) plus a test constant for
   install spec extensions.
"""

from __future__ import annotations

import functools
import sys
import types
from unittest.mock import MagicMock

# ---------------------------------------------------------------------------
# 1. Stub out cmk.dev_deploy.privilege
# ---------------------------------------------------------------------------

# Stub cmk.dev_deploy.site.overlay (depends on privilege, mount commands)
if "cmk.dev_deploy.site.overlay" not in sys.modules:
    _overlay_stub = types.ModuleType("cmk.dev_deploy.site.overlay")
    _overlay_stub.is_overlay_active = MagicMock(return_value=True)  # type: ignore[attr-defined]
    _overlay_stub.ensure_overlay = MagicMock()  # type: ignore[attr-defined]
    _overlay_stub.teardown_overlay = MagicMock()  # type: ignore[attr-defined]
    sys.modules["cmk.dev_deploy.site.overlay"] = _overlay_stub

if "cmk.dev_deploy.site.privilege" not in sys.modules:
    import dataclasses
    from pathlib import Path

    @dataclasses.dataclass
    class _SSHStateStub:
        """Test stub for SSHState."""

        deploy_key_path: Path | None = None
        ssh_available: dict[str, bool] = dataclasses.field(default_factory=dict)

        def clear_ssh_cache(self) -> None:
            self.ssh_available.clear()

    _stub = types.ModuleType("cmk.dev_deploy.site.privilege")
    _stub.SSHState = _SSHStateStub  # type: ignore[attr-defined]
    _stub.run_as_site_user = MagicMock()  # type: ignore[attr-defined]
    _stub.get_real_user = MagicMock(return_value="testuser")  # type: ignore[attr-defined]
    _stub.run_as_root = MagicMock()  # type: ignore[attr-defined]
    _stub.ensure_sudo = MagicMock()  # type: ignore[attr-defined]
    _stub.try_setcap = MagicMock(return_value=True)  # type: ignore[attr-defined]
    _stub.inject_ssh_key = MagicMock(return_value=True)  # type: ignore[attr-defined]
    _stub.clear_ssh_cache = MagicMock()  # type: ignore[attr-defined]
    sys.modules["cmk.dev_deploy.site.privilege"] = _stub

# ---------------------------------------------------------------------------
# 2. Seed manifest reader cache (tests must never read deploy_manifest.json)
# ---------------------------------------------------------------------------
# The manifest is gitignored (generated by Bazel queries at build time).
# We always replace _load_raw() with a seed built from checked-in data
# (deploy_specs.toml) + representative wheel/config specs.
#
# The only test constant is _TEST_INSTALL_SPEC_EXTENSIONS: which file types
# Bazel reports per compiled package.  Everything else flows from the real
# _compute_categorization_rules() function and the checked-in TOML.

import cmk.dev_deploy.manifest.reader as _reader

# Install spec extensions: the one piece of Bazel-derived data that tests
# cannot query.  Changes only when a package switches source languages.
_TEST_INSTALL_SPEC_EXTENSIONS: dict[str, frozenset[str]] = {
    "packages/check-cert": frozenset({".rs"}),
    "packages/check-http": frozenset({".rs"}),
    "packages/mk-oracle": frozenset({".rs"}),
    "packages/livestatus": frozenset({".cc", ".h", ".hpp"}),
    "packages/neb": frozenset({".cc", ".h", ".hpp"}),
    "packages/unixcat": frozenset({".cc", ".h"}),
    "non-free/packages/cmc": frozenset({".cc", ".h", ".hpp", ".proto"}),
    "packages/cmk-frontend": frozenset({".css", ".js", ".scss", ".ts"}),
    "packages/cmk-frontend-vue": frozenset({".js", ".ts", ".tsx", ".vue"}),
}


def _build_seed_manifest() -> dict[str, object]:
    """Build a seed manifest with computed categorization rules.

    Uses the real TOML and computation pipeline — only the install spec
    extensions are a test constant (Bazel is unavailable in tests).
    """
    from cmk.dev_deploy.manifest.update import (
        _compute_categorization_rules,
        _label_to_package_path,
        _load_specs_from_toml,
        _load_supplementary_rules,
        specs_path,
    )

    # Load install specs from the checked-in TOML and derive source_prefix
    toml_data = _load_specs_from_toml(specs_path(), is_nonfree_checkout=True)
    install_specs = toml_data["install_specs"]
    for spec in install_specs:
        if not spec.get("source_prefix"):
            spec["source_prefix"] = _label_to_package_path(spec["package_target"])

    # Representative wheel and config specs (covering paths used in tests)
    wheel_specs: list[dict[str, object]] = [
        {"source_prefix": "cmk"},
        {"source_prefix": "packages/cmk-ccc"},
        {"source_prefix": "packages/cmk-shared-typing"},
        {"source_prefix": "non-free/packages/cmk-bakery"},
    ]
    config_specs: list[dict[str, object]] = [
        {"source_prefix": "agents/", "method": "copy_dir"},
        {"source_prefix": "active_checks/", "method": "install_files"},
        {"source_prefix": "omd/", "method": "copy_dir"},
        {"source_prefix": "locale/", "method": "locale_compile"},
    ]

    manifest_data: dict[str, object] = {
        "install_specs": install_specs,
        "wheel_specs": wheel_specs,
        "config_specs": config_specs,
        "service_specs": toml_data["service_specs"],
        "deploy_deps": {},
    }

    supplementary = _load_supplementary_rules(specs_path())
    rules = _compute_categorization_rules(
        manifest_data, _TEST_INSTALL_SPEC_EXTENSIONS, supplementary
    )
    manifest_data["categorization_rules"] = rules

    return manifest_data


_SEED_MANIFEST = _build_seed_manifest()


@functools.lru_cache(maxsize=1)
def _seeded_load_raw() -> dict[str, object]:
    return _SEED_MANIFEST


_reader._load_raw = _seeded_load_raw  # noqa: SLF001
