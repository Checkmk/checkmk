class Checker:
    def __init__(
        self,
        *,
        omit_start_lines: int = 0,
        remove_prefix: str = "",
        previous_lines: None | list[str] = None,
        next_lines: None | list[str] = None,
    ) -> None:
        self.omit_start_lines = omit_start_lines
        self.remove_prefix = remove_prefix
        self.previous_lines = previous_lines or []
        self.next_lines = next_lines or []

    def _read(self, path: Path) -> list[str]:
        try:
            with path.open() as fo:
                for _ in range(self.omit_start_lines):
                    next(fo)
                return [
                    next(fo).removesuffix("\n").removeprefix(self.remove_prefix)
                    for _ in range(3 + len(self.previous_lines) + len(self.next_lines))
                ]
        except StopIteration:
            return []

    def check(self, path: Path, header: Sequence[re.Pattern[str]]) -> bool:
        result = []
        lines = self._read(path)
        if not lines:
            return False
        for line in self.previous_lines:
            result.append(lines.pop(0) == line)
        for line in self.next_lines:
            result.append(lines.pop(len(header)) == line)
        return all(chain(result, (regex.match(line) for regex, line in zip(header, lines))))


CHECKER = {
    ".py": Checker(previous_lines=["#!/usr/bin/env python3"], remove_prefix="# "),
    ".ts": Checker(previous_lines=["/**"], remove_prefix=" * ", next_lines=[" */"]),
    ".js": Checker(previous_lines=["/**"], remove_prefix=" * ", next_lines=[" */"]),
    ".sh": Checker(omit_start_lines=1, remove_prefix="# "),
    ".vue": Checker(previous_lines=["<!--"], next_lines=["-->"]),
    ".css": Checker(previous_lines=["/**"], remove_prefix=" * ", next_lines=[" */"]),
}


def check(suffix: str, abs_path: Path, rel_path: Path) -> bool:
    try:
        return CHECKER[suffix].check(
            abs_path,
            HEADER_CEE
            if rel_path.is_relative_to(Path("src/graph-designer"))
            or rel_path.is_relative_to(Path("src/metric-backend"))
            or rel_path in ENTERPRISE_LICENSED_FILES_FORM_SPECS
            else HEADER,
        )
    except Exception as e:
        raise RuntimeError(f"Could not find Checker for {rel_path}") from e


def test_license_headers() -> None:
    package_root = Path(__file__).resolve().parent.parent
    problems = []
    for root, _dirs, files in package_root.walk():
        relative_root = root.relative_to(package_root)

        if relative_root.parts and relative_root.parts[0] in ROOT_FOLDERS_IGNORED:
            continue
        for file in files:
            rel_path = relative_root / file  # Relative path for comparisons
            abs_path = root / file  # Absolute path for file operations

            if rel_path.suffix in SUFFIX_IGNORED:
                continue

            if any(rel_path.match(glob) for glob in GLOB_IGNORED):
                continue

            if rel_path in FILES_IGNORED:
                continue

            if not (suffix := rel_path.suffix):
                suffix = PATH_TO_SUFFIX[rel_path]

            if not check(suffix, abs_path, rel_path):
                problems.append(rel_path)

    assert len(problems) == 0, (
        "Please check the license header for the following files:\n"
        + "\n".join(str(p) for p in problems)
    )
